    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        if(jTextFieldFare.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Please enter Fare","swing Tester",JOptionPane.WARNING_MESSAGE);
        }
        
        try
      {
               Class.forName("com.mysql.jdbc.Driver");  
               Connection conn = DriverManager.getConnection("jdbc:mysql:///ams","root","");  

               String sql="insert into flight values(?, ?, ?, ?, ?)";
               PreparedStatement pstmt=conn.prepareStatement(sql);
                pstmt.setInt(1,Integer.parseInt(jTextFieldNo.getText()));
                pstmt.setString(2,jTextFieldName.getText());
                pstmt.setString(3,jTextFieldSource.getText());
               pstmt.setString(4,jTextFieldDestination.getText());
               pstmt.setString(5,jTextFieldFare.getText());
              

              pstmt.executeUpdate();
              JOptionPane.showMessageDialog(null,"Data Successfully Inserted in Table");
              conn.close();
      }
      catch(Exception e)
     {
           JOptionPane.showMessageDialog(null,e);
      }
    }//GEN-LAST:event_jButtonAddActionPerformed

