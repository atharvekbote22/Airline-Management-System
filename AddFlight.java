    private void jTextFieldFareKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldFareKeyPressed
        String age=jTextFieldFare.getText();
        int length=age.length();
        char c=evt.getKeyChar();

       if(evt.getKeyChar()>='0' && evt.getKeyChar()<='9')
       {
            if(length<7)
            {
                jTextFieldFare.setEditable(true);
            }
            else
            {
                jTextFieldFare.setEditable(false);
            }
       }
       else
       {    //allow backspace 
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE||evt.getExtendedKeyCode()==KeyEvent.VK_DELETE)
            {
                   jTextFieldFare.setEditable(true);
            }
            else
            {
                   jTextFieldFare.setEditable(false);
            }
       } 
    }//GEN-LAST:event_jTextFieldFareKeyPressed

